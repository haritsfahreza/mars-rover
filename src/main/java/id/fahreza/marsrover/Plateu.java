package id.fahreza.marsrover;

public class Plateu {

    private int maximumX = 0;
    private int maximumY = 0;

    public Plateu(int maximumX, int maximumY) {
        this.maximumX = maximumX;
        this.maximumY = maximumY;
    }

    public int getMaximumX() {
        return maximumX;
    }

    public int getMaximumY() {
        return maximumY;
    }

}
